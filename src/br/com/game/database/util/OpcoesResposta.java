package br.com.game.database.util;


public enum OpcoesResposta {

	RESP_1(1,"Sim, completamente"),
	RESP_2(2,"Sim, parcialmente"), 
	RESP_3(3,"Não, parcialmente"), 
	RESP_4(4,"Não, completamente");

	private int id;
	private String descricao;

	OpcoesResposta(int id, String descricao){
		this.id = id;
		this.descricao = descricao;
	}
	
	public int getId() {
		return id;
	}

	public String getDescricao() {
		return descricao;
	}


}
